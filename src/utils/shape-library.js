/**
 * Register shape to shapes library.
 * The shape is only can be path, polygon, or polyline.
 * @since 1.0.0
 * @param {string|array} label Translatable name of the shape, or a collection of shapes.
 * @param {string} type Svg tag name: 'path', 'polygon', or 'polyline'.
 * @param {string} path Path commands, or points separated by spaces.
 * @param {string} viewBox
 */

export function registerShape(label, type, path, viewBox) {
	if (isArray(label)){
		each(label, ({label, type, path, viewBox}) => registerShape(label, type, path, viewBox));
	} else {
		const validShape = (type === 'path' || type === 'polyline' || type === 'polygon');
		if (validShape && path){
			if (type === 'path'){
				path = serializePath(makePathSimpler(parsePath(path)));
			} else { // In case of polygon, or polyline.
				path = (path || '').replace(/\,/g, ' ');
			}
			shapeLibrary.push({
				label,
				type,
				path,
				viewBox
			});
		}
	}
}
